import { HttpError, IConnection } from "@nestia/fetcher";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";

import { IApiUsersEventsOrgs } from "../../../../structures/IApiUsersEventsOrgs";
import { event } from "../../../../structures/event";

/**
 * List organization events for the authenticated user.
 * This is the user's organization dashboard. You must be authenticated as the user to view this.
 *
 * > [!NOTE]
 * > This API is not built to serve real-time use cases. Depending on the time of day, event latency can be anywhere from 30s to 6h.
 *
 * @tag activity
 * @path users/:username/events/orgs/:org
 * @nestia Generated by Nestia - https://github.com/samchon/nestia
 */
export async function getByUsernameAndOrg(
  connection: IConnection<getByUsernameAndOrg.Headers>,
  username: string,
  org: string,
): Promise<getByUsernameAndOrg.Output> {
  return !!connection.simulate
    ? getByUsernameAndOrg.simulate(connection, username, org)
    : PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...getByUsernameAndOrg.METADATA,
          path: getByUsernameAndOrg.path(username, org),
          status: null,
        },
      );
}
export namespace getByUsernameAndOrg {
  export type Headers = IApiUsersEventsOrgs.GetHeader;
  export type Output = event[];

  export const METADATA = {
    method: "GET",
    path: "/users/:username/events/orgs/:org",
    request: null,
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = (username: string, org: string) =>
    `/users/${encodeURIComponent(username ?? "null")}/events/orgs/${encodeURIComponent(org ?? "null")}`;
  export const random = (g?: Partial<typia.IRandomGenerator>): event[] =>
    typia.random<event[]>(g);
  export const simulate = (
    connection: IConnection<getByUsernameAndOrg.Headers>,
    username: string,
    org: string,
  ): Output => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: getByUsernameAndOrg.path(username, org),
      contentType: "application/json",
    });
    try {
      assert.param("username")(() => typia.assert(username));
      assert.param("org")(() => typia.assert(org));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random(
      "object" === typeof connection.simulate && null !== connection.simulate
        ? connection.simulate
        : undefined,
    );
  };
}
